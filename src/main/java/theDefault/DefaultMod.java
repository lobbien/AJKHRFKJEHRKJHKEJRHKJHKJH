    @Override
    public void receivePostInitialize() {
        logger.info("Loading badge image and mod options");
        Texture badgeTexture = TextureLoader.getTexture(BADGE_IMAGE);
        ModPanel settingsPanel = new ModPanel();
        ModLabeledToggleButton enableNormalsButton = new ModLabeledToggleButton("This is the text which goes next to the checkbox.",
                350.0f, 700.0f, Settings.CREAM_COLOR, FontHelper.charDescFont, // Position (trial and error it), color, font
                enablePlaceholder,
                settingsPanel,
                (label) -> {},
                (button) -> {
            
            enablePlaceholder = button.enabled;
            try {
                SpireConfig config = new SpireConfig("defaultMod", "theDefaultConfig", theDefaultDefaultSettings);
                config.setBool(ENABLE_PLACEHOLDER_SETTINGS, enablePlaceholder);
                config.save();
            } catch (Exception e) {
                e.printStackTrace();
            }
        });
        settingsPanel.addUIElement(enableNormalsButton); // Add the button to the settings panel. Button is a go.
        BaseMod.registerModBadge(badgeTexture, MODNAME, AUTHOR, DESCRIPTION, settingsPanel);
        AestheticShader = new ShaderProgram(Gdx.files.internal("aestheticsResources/shader/aestheticcshadera/vertexShader.vs").readString(), Gdx.files.internal("aestheticsResources/shader/aestheticcshadera/fragShader.fs").readString());

        logger.info("Done loading badge Image and mod options");

        logger.info(AestheticShader.getLog());

    }
    
